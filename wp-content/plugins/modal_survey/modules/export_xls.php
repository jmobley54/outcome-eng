						$marker = "";
						if ( $personal ) {
							if ( in_array( $key, $user_votes[ $qkey ] ) ) {
								$marker = "* ";
							}
							$aoptions = unserialize( $answer[ 'aoptions' ] );
							if ( $aoptions[ 4 ] != "" && in_array( $key, $user_votes[ $qkey ] ) ) {
								$score = $aoptions[ 4 ];
								$totalscore += $score;
							}
							else {
								$score = "";
							}
							$objPHPExcelWriter->setActiveSheetIndex(0)
							->setCellValue('A'.$c, $marker . $answer['answer'])->setCellValue('B'.$c, (string)$answer['count'])->setCellValue('C'.$c, (string)$answer['percentage'].'%')->setCellValue('D'.$c, $score)->getStyle('A'.$c)->applyFromArray($csvheader_normal_row);
							$objPHPExcelWriter->getActiveSheet()->getStyle('A'.$c.':D'.$c)->getAlignment()->setWrapText(true)->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_LEFT);
