				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Question / Answer', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, __( 'Votes', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('C'.$c, __( 'Percentage', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('D'.$c, __( 'Score', MODAL_SURVEY_TEXT_DOMAIN ))->getStyle('A'.$c.':D'.$c)->applyFromArray($csvheader_row);
			}
			else {
				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Question / Answer', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, __( 'Votes', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('C'.$c, __( 'Percentage', MODAL_SURVEY_TEXT_DOMAIN ))->getStyle('A'.$c.':C'.$c)->applyFromArray($csvheader_row);		
			}
			$c++;
