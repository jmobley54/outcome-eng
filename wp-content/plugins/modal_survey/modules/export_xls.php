				$c++;
					$objPHPExcelWriter->setActiveSheetIndex(0)
					->setCellValue('A'.$c, __( 'Participant answers marked with stars: *', MODAL_SURVEY_TEXT_DOMAIN ) )->getStyle('A'.$c.':C'.$c)->applyFromArray($csvheader_normal_row);;
			}
