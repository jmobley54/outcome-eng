							 ->setSubject($survey_exp['name'])
							 ->setDescription($survey_exp['name']." " . __( 'generated by', MODAL_SURVEY_TEXT_DOMAIN ) . " Modal Survey");

$csvheader_row = array(
    'font'  => array(
        'bold'  => true,
        'color' => array('rgb' => '000000'),
        'size'  => 10,
        'name'  => 'Helvetica'
    ));
$csvheader_normal_row = array(
    'font'  => array(
        'bold'  => false,
        'color' => array('rgb' => '000000'),
        'size'  => 10,
        'name'  => 'Helvetica'
    ));

			$c=1;
			$objPHPExcelWriter->getActiveSheet()->getColumnDimension('A')->setWidth(60);
			$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Survey ID', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, $survey_exp['id'])->getStyle('A'.$c)->applyFromArray($csvheader_row);
			$c++;
			$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Survey Name', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, $survey_exp['name'])->getStyle('A'.$c)->applyFromArray($csvheader_row);
			$c++;
			$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Generated', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, str_replace("'", "", $survey_exp['export_time']))->getStyle('A'.$c)->applyFromArray($csvheader_row);
			$c++;
			$objPHPExcelWriter->getActiveSheet()->getStyle('A'.$c.':B'.$c)->getAlignment()->setWrapText(true)->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_LEFT);
			if ( $personal ) {
				$c++;
				$objPHPExcelWriter->getActiveSheet()->getColumnDimension('A')->setWidth(60);
				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'User ID', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, $survey_exp[ 'user_details' ]->autoid )->getStyle('A'.$c)->applyFromArray($csvheader_row);
				$c++;
				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Username', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, ( $survey_exp[ 'user_details' ]->username ? $survey_exp[ 'user_details' ]->username : __( 'Not Specified', MODAL_SURVEY_TEXT_DOMAIN ) ))->getStyle('A'.$c)->applyFromArray($csvheader_row);
				$c++;
				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Created', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, $survey_exp[ 'user_details' ]->created)->getStyle('A'.$c)->applyFromArray($csvheader_row);
				$objPHPExcelWriter->getActiveSheet()->getStyle('A'.$c.':B'.$c)->getAlignment()->setWrapText(true)->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_LEFT);
				$c++;
				$objPHPExcelWriter->getActiveSheet()->getColumnDimension('A')->setWidth(60);
				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Email', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, $survey_exp[ 'user_details' ]->email)->getStyle('A'.$c)->applyFromArray($csvheader_row);
				$c++;
				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Name', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, $survey_exp[ 'user_details' ]->name)->getStyle('A'.$c)->applyFromArray($csvheader_row);
				$objPHPExcelWriter->getActiveSheet()->getStyle('A'.$c.':B'.$c)->getAlignment()->setWrapText(true)->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_LEFT);
				$c++;
				if ( ! empty( $survey_exp[ 'user_details' ]->custom ) ) {
					foreach ( unserialize( $survey_exp[ 'user_details' ]->custom ) as $muc_index=>$muc ) {
						$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, ucfirst( strtolower( $muc_index ) ))->setCellValue('B'.$c, $muc)->getStyle('A'.$c)->applyFromArray($csvheader_row);
						$objPHPExcelWriter->getActiveSheet()->getStyle('A'.$c.':B'.$c)->getAlignment()->setWrapText(true)->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_LEFT);
						$c++;
					}
				}
				$objPHPExcelWriter->getActiveSheet()->getColumnDimension('A')->setWidth(60);
				$objPHPExcelWriter->setActiveSheetIndex(0)->setCellValue('A'.$c, __( 'Total Score', MODAL_SURVEY_TEXT_DOMAIN ))->setCellValue('B'.$c, $survey_exp[ 'user_details' ]->allscore)->getStyle('A'.$c)->applyFromArray($csvheader_row);
